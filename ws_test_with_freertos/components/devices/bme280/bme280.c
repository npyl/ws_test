void bme280_init( S_I2C_INFO *info )
{
    const S_I2C_CONFIG * const p_cfg = i2cGetConfig();

    /* Read the device id
     *   Send command: 0xD0
     *   Should read: 0x60
     */
    i2c_read( info->id, info->dev_id, p_cfg[ info->id ].device[ info->dev_id ].id_reg, &ch, 1u );
}

